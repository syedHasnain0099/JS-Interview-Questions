// Native objects are objects defined by the ECMAScirpt Specification which is strandard that Javascript is based on
// These objects are part of the core language and are available in any javascript enviorment
// Examples of native objects are
// Global Objects are: Object, Function, Array, String, Number, Boolean, Date
// Math and json objects: Math, JSON

// Host Objects are objects provided by the enviorment in which javascript is running. These objects are not defined by
// ECMAScript specification but the host enviorment such as web browser or node enviorment
// In a Web Browser:

// DOM objects: document, window, HTMLElement, NodeList, Event
// Browser-specific APIs: fetch, XMLHttpRequest, localStorage, sessionStorage
// In Node.js:

// Node.js-specific APIs: require, module, exports, Buffer, process
